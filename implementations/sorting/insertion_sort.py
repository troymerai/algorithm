"""
=============================================================
 삽입 정렬 (Insertion Sort) 구현 가이드
=============================================================
 핵심 아이디어:
   카드를 한 장씩 받아서 이미 정렬된 손패의 올바른 위치에
   끼워 넣는 것과 같은 원리

 시간복잡도:
   Best    : O(n)       ← 이미 정렬된 경우 (비교만 n-1번)
   Average : O(n²)
   Worst   : O(n²)      ← 역순 정렬된 경우

 공간복잡도: O(1) — In-place 정렬

 시험 포인트:
   - "거의 정렬된 배열"에서 가장 효율적인 정렬 → 삽입 정렬
   - 작년 문제 (9)번: 삽입 정렬은 거의 정렬된 배열에서 비효율적? → 거짓!
=============================================================
"""


def insertion_sort(arr):
    """
    동작 과정:
    [5, 2, 4, 6, 1, 3]  ← 원본
     ^  이미 정렬됨(길이 1)
    
    i=1: key=2, [2, 5, 4, 6, 1, 3]  ← 2를 5 앞에 삽입
    i=2: key=4, [2, 4, 5, 6, 1, 3]  ← 4를 5 앞에 삽입
    i=3: key=6, [2, 4, 5, 6, 1, 3]  ← 6은 제자리
    i=4: key=1, [1, 2, 4, 5, 6, 3]  ← 1을 맨 앞에 삽입
    i=5: key=3, [1, 2, 3, 4, 5, 6]  ← 3을 2와 4 사이에 삽입
    """
    n = len(arr)
    
    for i in range(1, n):
        key = arr[i]          # 현재 삽입할 원소
        j = i - 1             # 정렬된 부분의 마지막 인덱스
        
        # key보다 큰 원소들을 오른쪽으로 한 칸씩 이동
        while j >= 0 and arr[j] > key:
            arr[j + 1] = arr[j]
            j -= 1
        
        arr[j + 1] = key      # 올바른 위치에 삽입
    
    return arr


# ── 테스트 ──
if __name__ == "__main__":
    test_cases = [
        [5, 2, 4, 6, 1, 3],
        [1, 2, 3, 4, 5],         # Best case: 이미 정렬됨
        [5, 4, 3, 2, 1],         # Worst case: 역순
        [3, 1, 2, 3, 1],         # 중복 포함
    ]
    
    for tc in test_cases:
        original = tc.copy()
        result = insertion_sort(tc)
        print(f"{original} → {result}")