import math

def floyd_warshall(n, w):
    """
    n: 정점 수 (정점은 0..n-1로 인덱싱)
    w: n x n 가중치 행렬
       - w[i][i] = 0 (자기 자신까지 거리는 0)
       - w[i][j] = math.inf (직접 간선 없음)
       - w[i][j] = 가중치 (i→j 직접 간선의 가중치)
    반환: d[i][j] = i에서 j까지의 최단 거리 (모든 정점 쌍 = APSP)
    """
    # d^0_ij ← w_ij : 초기 거리 행렬은 입력 가중치 행렬과 동일
    # → 중간 정점을 허용하지 않은 상태 (직접 간선만 사용한 거리)
    # → row[:]로 각 행을 복사하여 입력 w를 보존 (in-place 갱신이 원본을 망가뜨리지 않게)
    d = [row[:] for row in w]

    # k 루프: 중간 정점으로 허용하는 집합을 {0} → {0,1} → ... → {0,...,n-1}로 단계적으로 확장
    # → k는 반드시 가장 바깥 루프 (안쪽에 두면 d^(k-1) 보장이 깨져 알고리즘 망가짐)
    # → in-place 갱신 안전성: k 루프 동안 d[i][k], d[k][j]는 변하지 않음 (d[k][k]=0이라서)
    for k in range(n):
        # i 루프: 시작 정점
        for i in range(n):
            # j 루프: 도착 정점
            for j in range(n):
                # 점화식: d^k_ij = min(d^(k-1)_ij,  d^(k-1)_ik + d^(k-1)_kj)
                # → "k를 거치는 새 경로(i → ... → k → ... → j)"가 "기존 i→j 경로"보다 짧으면 갱신
                if d[i][k] + d[k][j] < d[i][j]:
                    d[i][j] = d[i][k] + d[k][j]

    # 모든 정점이 중간 정점 후보가 된 d^n 상태 = 모든 정점 쌍의 진짜 최단 거리
    return d


# 예시 (가중 유향 그래프, n=4)
INF = math.inf
w = [
    [0,   3,   INF, 7  ],   # 정점 0: 0→1=3, 0→3=7
    [8,   0,   2,   INF],   # 정점 1: 1→0=8, 1→2=2
    [5,   INF, 0,   1  ],   # 정점 2: 2→0=5, 2→3=1
    [2,   INF, INF, 0  ],   # 정점 3: 3→0=2
]
d = floyd_warshall(4, w)
for row in d:
    print(row)
# 출력: 모든 정점 쌍의 최단 거리 행렬
# [0, 3, 5, 6]    ← 0에서 각 정점까지 최단 거리
# [5, 0, 2, 3]    ← 1에서 각 정점까지 최단 거리
# [3, 6, 0, 1]    ← 2에서 각 정점까지 최단 거리
# [2, 5, 7, 0]    ← 3에서 각 정점까지 최단 거리